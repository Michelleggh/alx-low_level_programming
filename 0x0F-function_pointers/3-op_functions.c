/**
 *op_add - Add two integers
 *
 *@a: integer 1
 *@b: integer 2
 *
 *Return: Integer
 */
int op_add(int a, int b)
{
return (a + b);
}

/**
 *op_sub - Sub two intergers
 *
 *@a: integer 1
 *@b:integer 2
 *
 *Return: Integer
 */
int op_sub(int a, int b)
{
return (a - b);
}
/**
 * op_mul - Product of two integers
 *
 *@a: integer 1
 *@b: integer 2
 *
 *Return: Integer
 */
int op_mul(int a, int b)
{
return (a * b);
}
/**
 *op_div - Result of the dividion os two integers
 *
 *@a: integer 1
 *@b: integer 2
 *
 *Return: Integer
 */
int op_div(int a, int b)
{
return (a / b);
}
/**
 *op_mod - Remainder of the division
 *
 *@a: integer 1
 *@b: integer 2
 *
 *Return: Integer
 */
int op_mod(int a, int b)
{
return (a % b);
}
